        f'  - Art. 1.008: Julgamento por maioria nos tribunais\n'
        f'  - Arts. 1.009-1.014: Apelação (contra sentença)\n'
        f'  - Arts. 1.015-1.020: Agravo de instrumento (decisões interlocutórias)\n'
        f'  - Arts. 1.021: Agravo interno\n'
        f'  - Arts. 1.022-1.026: Embargos de declaração\n'
        f'  - Arts. 1.027-1.028: Recurso ordinário\n'
        f'  - Arts. 1.029-1.041: Recurso especial e recurso extraordinário\n'
        f'  - Arts. 1.042-1.044: Agravo em recurso especial e extraordinário\n'
        f'\n'
        f'LEGISLAÇÃO PROCESSUAL — ADMISSIBILIDADE\n'
        f'- Art. 1.009: Apelação — cabimento contra sentença, efeito devolutivo amplo\n'
        f'- Art. 1.009, §1º: Questões resolvidas na fase de conhecimento (impugnáveis em\n'
        f'  preliminar de apelação, se não cabe agravo de instrumento)\n'
        f'- Art. 1.015, I-XIII: Hipóteses de agravo de instrumento (rol taxativo mitigado)\n'
        f'- Tema 988/STJ: Taxatividade mitigada do art. 1.015 — cabimento de agravo\n'
        f'  quando houver urgência + inutilidade do julgamento da apelação\n'
        f'- Art. 1.022: Embargos de declaração — obscuridade, contradição, omissão\n'
        f'- Art. 1.025: Prequestionamento ficto (matéria suscitada em embargos)\n'
        f'- Art. 1.029: Recurso especial (contrariedade a lei federal) e extraordinário\n'
